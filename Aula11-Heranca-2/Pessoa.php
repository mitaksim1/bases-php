<?php

// As classes abstratas nao podem ser instanciadas, elas serao usadas apenas para heranças
abstract class Pessoa {
    // como queremos que as classes filhas tenham acesso aos atributos da classe mae os passamos em modo protected em vez de private
    protected $nome;
    protected $idade;
    protected $sexo;

    // final function: método que nao pode ser sobescrito
    public final function fazerAniv() {
        $this->idade ++;
    }

    /**
     * Get the value of nome
     */ 
    public function getNome()
    {
        return $this->nome;
    }

    /**
     * Set the value of nome
     *
     * @return  self
     */ 
    public function setNome($nome)
    {
        $this->nome = $nome;

        return $this;
    }

    /**
     * Get the value of idade
     */ 
    public function getIdade()
    {
        return $this->idade;
    }

    /**
     * Set the value of idade
     *
     * @return  self
     */ 
    public function setIdade($idade)
    {
        $this->idade = $idade;

        return $this;
    }

    /**
     * Get the value of sexo
     */ 
    public function getSexo()
    {
        return $this->sexo;
    }

    /**
     * Set the value of sexo
     *
     * @return  self
     */ 
    public function setSexo($sexo)
    {
        $this->sexo = $sexo;

        return $this;
    }
}